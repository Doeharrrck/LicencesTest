namespace LicenseHandling
{
    /// <summary>
    /// Enum type gor the buttons to show in the Message Dialog.
    /// </summary>
    public enum MessageBoxButtons
    {
        /// <summary>
        /// The message box displays an OK button.
        /// </summary>
        OK = 0,

        /// <summary>
        /// The message box displays OK and Cancel buttons.
        /// </summary>
        OKCancel = 1,

        /// <summary>
        /// The message box displays Abort, Retry, and Cancel buttons.
        /// </summary>
        AbortRetryIgnore = 2,

        /// <summary>
        /// The message box displays Yes, No, and Cancel buttons.
        /// </summary>
        YesNoCancel = 3,

        /// <summary>
        /// The message box displays Yes and No buttons.
        /// </summary>
        YesNo = 4,

        /// <summary>
        /// The message box displays Cancel button.
        /// </summary>
        Cancel = 5,
    }
}
